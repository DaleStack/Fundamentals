x = tuple(("Apple", "Orange", "Banana"))   #tuple
x = range(5)   #range

# Mapping Type
x = dict(name="John", age=30)  #dict

# Set Types
x = set(("Apple", "Orange", "Banana"))  #set
x = frozenset(("Apple", "Orange", "Banana")) #frozenset

# Boolean Type
x = bool(1)       #bool

# Binary Types
x = bytes(5)   #bytes
x = bytearray(5)         #bytearray
