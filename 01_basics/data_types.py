"""Data types in Python."""

# Text Type
x = "This is a statement." # str

# Numeric Types 
x = 10         #int
x = 20.5       #float
x = 1j         #complex

# Sequence Types
x = ["Apple", "Orange", "Banana"]  #list
x = ("Apple", "Orange", "Banana")  #tuple
x = range(5)   #range

# Mapping Type
x = {"name": "Alice", "age": 25}   #dict

# Set Types
x = {"Apple", "Orange", "Banana"}  #set
x = frozenset({"Apple", "Orange", "Banana"}) #frozenset

# Boolean Type
x = True       #bool

# Binary Types
x = b"Hello"   #bytes
x = bytearray(5)         #bytearray
x = memoryview(bytes(5)) #memoryview

# None Type
x = None       #NoneType

"""Setting specific data types in Python using constructor functions."""

# Text Type
x = str("This is a statement.")

# Numeric Types 
x = int(10)         #int
x = float(20.5)     #float
x = complex(1j)     #complex

# Sequence Types
