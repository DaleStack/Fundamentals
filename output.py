"""The print() function in Python is used to output data to the console. It can take multiple arguments and 
can format the output in various ways."""

# This script prints "Hello, World!" to the console.
print("Hello, World!")


# The print function can also take multiple arguments
print("Hello,", "World!", 123)

# You can also have separators and end characters
print("Hello", "World", sep=", ", end="!!!\n")

print() # Just to add a newline after the previous print statement

# Printing formatted strings
name = "Alice"
age = 30
print(f"{name} is {age} years old.")



